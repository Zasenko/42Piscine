#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		str++;
		i++;
	}
	return (i);
}

int	main(void)
{
	char	str[7] = "Dima!";

	printf("String: %s\nCount: %d\n", str, ft_strlen(str));
}
